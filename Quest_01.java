package Exercico_20;

public class Quest_01 {
    /**
     * Faça um algoritmo para escrever os números entre 1 e 1000.
     */
    public static void main(String[] args) {        
        int i;
        i = 1;
        while (i <= 1000) {
            System.out.println(i);
            i = i + 1;
        }
    }
    
}
